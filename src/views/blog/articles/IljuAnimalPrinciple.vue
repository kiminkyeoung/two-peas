        <li><strong>적색 (火):</strong> 병(丙), 정(丁) - 불의 기운, 열정과 확산을 상징. (예: 병인(丙寅) 붉은 호랑이)</li>
        <li><strong>황색 (土):</strong> 무(戊), 기(己) - 흙의 기운, 중용과 신뢰를 상징. (예: 무진(戊辰) 황색 용)</li>
        <li><strong>백색 (金):</strong> 경(庚), 신(辛) - 쇠의 기운, 결단과 의리를 상징. (예: 경신(庚申) 하얀 원숭이)</li>
