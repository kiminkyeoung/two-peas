      <h2>1. 사주팔자, 여덟 글자의 비밀</h2>
      <p>
        우리의 운명을 해석하는 기본 틀인 '사주팔자(四柱八字)'는 태어난 연(年), 월(月), 일(日), 시(時)의 네 기둥(四柱)과, 각 기둥을 나타내는 두 글자씩, 총 여덟 글자(八字)로 이루어집니다. 이 여덟 글자는 각각 '천간(天干)'과 '지지(地支)'라는 요소로 구성됩니다.
      </p>
