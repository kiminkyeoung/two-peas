        <li><strong>지지(地支):</strong> 땅의 기운. 자(子), 축(丑), 인(寅), 묘(卯), 진(辰), 사(巳), 오(午), 미(未), 신(申), 유(酉), 술(戌), 해(亥)의 12가지가 있습니다. 현실, 행동, 내부적인 환경을 상징하며, 우리가 흔히 아는 12가지 띠 동물이 바로 이 지지에 해당합니다.</li>
      </ul>
